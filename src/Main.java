// Declaração da classe principal do programa
public class Main {

    // Método principal - ponto inicial de execução do programa Java
    public static void main(String[] args) {

        // Variável inteira que armazena a idade
        int idade = 25;

        // Variável do tipo double que armazena um salário com casas decimais
        double salarioMensal = 4500.00;

        // Variável do tipo char que armazena um único caractere representando o gênero
        char genero = 'M';

        // Variável booleana que indica verdadeiro ou falso
        // Aqui indica se a pessoa está empregada trabalhando com Java
        boolean empregadoJava = true;

        // Exibe no console a idade armazenada na variável idade
        System.out.println("Idade: " + idade);

        // Exibe no console o valor do salário mensal
        System.out.println("Salário: " + salarioMensal);

        // Exibe no console o gênero armazenado na variável genero
        System.out.println("Gênero: " + genero);

        // Exibe no console o valor booleano indicando se está empregado com Java
        System.out.println("Está empregado? " + empregadoJava);
    }
}